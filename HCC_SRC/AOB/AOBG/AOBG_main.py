import numpy as np
import os
from scipy.stats import ortho_group

class AOBG:
    """
    AOBG class for generating and saving various vectors and matrices used in optimization problems.

    This class provides methods for generating the following components:
    - Ovector: Initial vector within the specified bounds.
    - Pvector: Permutation vector.
    - Wvector: Weight vector based on a normal distribution.
    - Rotation matrices: Orthogonal rotation matrices for each subgroup size.
    - S vector: Subgroup sizes.
    - Design matrix: A matrix representing the interaction between subgroups.
    - Function info: Stores general information about the problem setup.
    """
    
    def __init__(self, fun_ID, seed, dimension, 
                 lower_bound, upper_bound, sub_num, subgroups, overlap_list, file_path):
        """
        Initializes the AOBG class with the provided parameters.

        Parameters:
        ----------
        fun_ID : int
            Function identifier used for naming the output files.
        seed : int
            The seed value for random number generation.
        dimension : int
            The dimensionality of the problem.
        lower_bound : float
            The lower bound of the problem space.
        upper_bound : float
            The upper bound of the problem space.
        sub_num : int
            The number of subgroups.
        subgroups : list
            A list of subgroup sizes.
        overlap_list : list
            A list of the number of overlapping variables for each subgroup.
        file_path : str
            Directory path where files will be saved.
        """
        self.fun_ID = fun_ID
        self.seed = seed
        self.dimension = dimension  # The dimensionality after expansion
        self.lower_bound = lower_bound
        self.upper_bound = upper_bound
        self.sub_num = sub_num  # Number of subgroups
        self.subgroups = subgroups  # Subgroup sizes
        self.overlap_list = overlap_list  # Degree of overlap between subgroups
        self.file_path = file_path  # Path to save the files
        self.dimension_real = self.dimension + np.sum(self.overlap_list) # Real dimensionality after considering overlaps

        # Set random seed based on dimension and seed value
        np.random.seed(self.dimension + self.seed)
        # Ensure the directory for the file path exists
        os.makedirs(os.path.dirname(self.file_path), exist_ok=True)

    def Ovector_make(self):
        """
        Generate the Ovector (initial vector) and save it to a file.

        The Ovector is generated within the specified bounds using a uniform distribution.
        It is then saved to a text file named 'F{fun_ID}-xopt.txt'.
        """
        Ovector = self.lower_bound + (self.upper_bound - self.lower_bound) * np.random.uniform(size=self.dimension).astype(np.float64)
        file_path_ = self.file_path + f'/F{self.fun_ID}-xopt.txt'
        with open(file_path_, 'w') as file:
            for value in Ovector:
                file.write(f"{value}\n")

    def Pvector_make(self): 
        """
        Generate the Pvector (permutation vector) and save it to a file.

        The Pvector is generated by creating a random permutation of integers from 1 to the dimensionality.
        It is saved to a text file named 'F{fun_ID}-p.txt'.
        """
        np.random.seed(self.dimension + self.seed)
        Pvector = np.random.permutation(self.dimension).astype(np.int64) + 1  # Start from 1
        self.Pvector = Pvector
        file_path_ = self.file_path + f'/F{self.fun_ID}-p.txt'
        with open(file_path_, 'w') as file:
            file.write(",".join(map(str, Pvector)))

    def w_make(self): 
        """
        Generate the Wvector (weight vector) and save it to a file.

        The Wvector is generated using a random normal distribution, with values scaled by a factor of 10^3.
        It is saved to a text file named 'F{fun_ID}-w.txt'.
        """
        np.random.seed(self.dimension + self.seed)
        Wvector = 10**(3 * np.random.randn(self.sub_num)).astype(np.float64)
        file_path_ = self.file_path + f'/F{self.fun_ID}-w.txt'
        with open(file_path_, 'w') as file:
            for value in Wvector:
                file.write(f"{value}\n")

    def rotation_matrix_make(self):
        """
        Generate random orthogonal rotation matrices for each unique subgroup size.

        For each unique subgroup size, an orthogonal rotation matrix is generated using scipy's ortho_group.
        These matrices are saved to files named 'F{fun_ID}-R{dims}.txt', where 'dims' is the subgroup size.
        """
        subgroups_type = list(set(self.subgroups))  # Get unique subgroup sizes
        self.subgroups_type = subgroups_type
        np.random.seed(self.dimension + self.seed)
        for dims in subgroups_type:
            matrix = ortho_group.rvs(dim=dims).astype(np.float64)
            file_path_ = self.file_path + f'/F{self.fun_ID}-R{dims}.txt'
            np.savetxt(file_path_, matrix, delimiter=',')

    def s_size_make(self): 
        """
        Set the subgroup sizes and save them to a file.

        The sizes are stored in a numpy array and saved to a text file named 'F{fun_ID}-s.txt'.
        """
        S = np.array(self.subgroups, dtype=np.float64)
        file_path_ = self.file_path + f'/F{self.fun_ID}-s.txt'
        with open(file_path_, 'w') as file:
            for value in S:
                file.write(f"{value}\n")

    def design_matrix_make(self):
        """
        Generate and save the design matrix representing the interaction between subgroups.

        The design matrix is created based on the permutation vector and subgroup definitions.
        It is saved to a text file named 'F{fun_ID}-design.txt'.
        """
        design_matrix = np.zeros((self.dimension, self.dimension), dtype=np.int64)
        begin_index = 0
        subgroups_real = []
        for i in range(self.sub_num):
            subgroup_length = self.subgroups[i]
            end_index = begin_index + subgroup_length
            subgroups_iter = self.Pvector[begin_index:end_index]
            subgroups_real.append(subgroups_iter)
            if i != len(self.subgroups) - 1:
                begin_index = end_index - self.overlap_list[i]

        # Efficiently assign values to the design matrix using NumPy
        for groups in subgroups_real:
            indices = np.array(groups)
            design_matrix[np.ix_(indices - 1, indices - 1)] = 1

        file_path_ = self.file_path + f'/F{self.fun_ID}-design.txt'
        np.savetxt(file_path_, design_matrix, delimiter=',')

    def fun_info_make(self):
        """
        Save general information about the function to a text file.

        This includes the function ID, dimensionality, bounds, subgroups, overlap degree, and other relevant details.
        The information is saved to a file named 'F{fun_ID}-info.txt'.
        """
        file_path_ = self.file_path + f'/F{self.fun_ID}-info.txt'
        with open(file_path_, 'w') as file:
            file.write(f"best: 0.0\n")
            file.write(f"dimension: {self.dimension}\n")
            file.write(f"lower_bound: {self.lower_bound}\n")
            file.write(f"upper_bound: {self.upper_bound}\n")
            file.write(f"sub_num: {self.sub_num}\n")
            file.write(f"subgroups: {self.subgroups}\n")
            file.write(f"overlap_degree: {self.overlap_list}\n")
            file.write(f"dimension_real: {self.dimension_real}\n")
            file.write(f"subgroups_type: {self.subgroups_type}\n")

    def main(self):
        """
        Execute the main function to generate all necessary data and save it to files.

        This function calls the methods to generate and save the Ovector, Pvector, Wvector, rotation matrices, 
        subgroup sizes, design matrix, and function-related information.
        """
        self.Ovector_make()
        self.Pvector_make()
        self.w_make()
        self.rotation_matrix_make()
        self.s_size_make()
        self.design_matrix_make()
        self.fun_info_make()





